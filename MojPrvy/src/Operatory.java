
public class Operatory {

	public static void main(String[] args) {
		
		//aritmeticke operatory
		System.out.println(10 + 5);
		System.out.println(10 - 5);
		System.out.println(10 * 5);
		System.out.println(10 / 5);
		System.out.println(10 % 5);
		
		System.out.println("---------------");
		//relacne operatory
		System.out.println(10 < 5);
		System.out.println(10 > 5);
		System.out.println(10 <= 5);
		System.out.println(10 >= 5);
		System.out.println(10 == 5);
		System.out.println(10 != 5);
		
		//logicke op.
		System.out.println((10 < 5)&&!(10 <= 5)||(10 != 5));
		
	}

}
